package Almoxarifado;

public class Codinterno {

	private int upc;
	private int receptaculo;
	private int corredor;
	
	public int getUpc() {
		return upc;
	}
	public void setUpc(int upc) {
		this.upc = upc;
	}
	public int getReceptaculo() {
		return receptaculo;
	}
	public void setReceptaculo(int receptaculo) {
		this.receptaculo = receptaculo;
	}
	public int getCorredor() {
		return corredor;
	}
	public void setCorredor(int corredor) {
		this.corredor = corredor;
	}
}
